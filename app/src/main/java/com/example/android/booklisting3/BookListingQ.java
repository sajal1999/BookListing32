package com.example.android.booklisting3;




public class BookListingQ {


    private String mAuthor;
    private String mTitle;
    private String mUrl;

    public BookListingQ(String author, String title, String url) {
    mAuthor=author;
    mTitle=title;
    mUrl=url;
    }

    public String getAuthor() {
        return mAuthor;
    }

    public String getTitle() {
        return mTitle;


    }
    public String getUrl() {return mUrl;}
}





